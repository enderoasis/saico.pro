<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>5. Вопросы
пользователей, оставленные без внимания</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Довольно
часто пользователи комментируют
фотографии, например, высказывают
положительное или отрицательное мнение
касательно какого-либо товара, задают
вопросы, связанные с характеристиками
продукта, способами оплаты, доставки и
т. д. Владельцу или менеджеру аккаунта
нужно обязательно отвечать на все эти
вопросы, поддерживать обратную связь
с клиентами. Ведь если вы не будете
читать их комментарии и отвечать на
вопросы, то люди могут подумать, что за
аккаунтом никто не следит и вам до них
просто нет дела. В результате желание
покупать товар отпадет само собой и
пользователей от вас отпишется или,
того хуже, уйдет к вашему более
внимательному конкуренту.</FONT></FONT></P>
<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>6. Массовая
простановка лайков и массовая подписка
на аккаунты пользователей</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Все еще
поддерживаю все ошибки, описанные в
этой статье, включая накрутки, но больше
не отношусь негативно к автоматизации.
Если это делать с умом, то можно получать
вполне неплохие результаты. </FONT></FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Как правило,
это делают для того, чтобы засветиться
перед максимально большим количеством
людей, привлечь к себе внимание
потенциальных клиентов. Реальной пользы
от подобных действий не так-то и много,
ведь многим привлеченным пользователям
ваши товары и услуги будут не интересны.
К тому же, массфоловинг и&nbsp;масслайкинг
не придают серьезности вашему бренду,
так как выглядите вы при этом жалко.
Есть огромное количество эффективных
маркетинговых шагов, которые вы можете
предпринять, чтобы привлечь пользователей
к вашему аккаунту — конкурсы, </FONT></FONT><A HREF="http://www.pro-smm.com/social-media-buttons/"><FONT COLOR="#0000ff"><FONT FACE="Times New Roman, serif"><FONT SIZE=3><U>SMO</U></FONT></FONT></FONT></A><FONT FACE="Times New Roman, serif"><FONT SIZE=3>,
оповещение реальных клиентов и т. д.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<IMG SRC="7__html_7e59224f.jpg" NAME="Рисунок 1" ALIGN=BOTTOM WIDTH=398 HEIGHT=204 BORDER=0></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Использовать
массфоловинг и&nbsp;масслайкинг целесообразно
лишь в том случае, когда ваш бизнес
относится к какой-то узкой тематике и
вам любыми способами нужно набрать
целевую аудиторию. Эти методы отнимают
огромное количество времени при очень
скромных результатах (макс. 30% ответят
вам взаимностью). Лучше потратить это
время на что-то более стоящее, что
принесет вам результаты в среднесрочной
и долгосрочной перспективе, чем
размениваться по мелочам, лайкая все
подряд.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>7. Публикация
постов лишь в вечернее время</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Подобное
мнение бытует среди людей, которые
занимаются продвижением в соц. сети
Вконтакте. Во многих пабликах ВКонтакте
новые посты публикуются ближе к вечеру,
так как именно в это время пользователи
возвращаются домой со своей работы,
учебы и т. д. Именно вечером большинству
пользователей удобно просматривать
свои странички в социальной сети, листать
новости. Что касается Инстаграм, то
здесь дела обстоят совершено по-другому.
Судите сами — Инстаграм является
мобильным приложением (для просмотра
контента люди используют, главным
образом смартфоны или планшетники). По
этой причине, пользовательская активность
по времени суток распределяется
более&nbsp;или&nbsp;менее равномерно. К тому
же, многие пользователи любят просматривать
ленту сразу после пробуждения, во время
уроков/работы и перед сном, поэтому
публикуя только в одно и то же время, вы
многое теряете!</FONT></FONT></P>
