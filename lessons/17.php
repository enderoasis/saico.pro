всего несколько секунд, но имеет высокую
эффективность.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Необходимо
также найти страницы, которые имеют
сходство по интересам, и сделать то же
самое. Этот процесс позволяет удвоить
или даже утроить целевую аудиторию, но
при этом получить пользователей, которые
заинтересованы в вашем продукте именно
