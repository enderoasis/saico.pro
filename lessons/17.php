<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>Влияние
таргетирования на конверсию рекламных
объявлений</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Instagram входит
в число крупнейших рекламных площадок,
которые используются для <A HREF="https://seoquick.com.ua/seo-this-year/">продвижения
товаров и услуг</A> в интернете. Рекламой
в </FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3><SPAN LANG="en-US">Instagram</SPAN></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>&nbsp;с
успехом пользуются крупные рекламодатели.
Однако почти две трети небольших компаний
негативно отзываются об эффективности
этого канала продвижения. Почему так
происходит?</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Основная
причина — это неправильный выбор целевой
аудитории. На первый взгляд, процесс
создания рекламных объявлений в </FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3><SPAN LANG="en-US">Instagram</SPAN></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>
не представляет никакой сложности. Всё,
что нужно, это просто&nbsp;зайти в Бизнес
Менеджер, создать новый критерий отбора
ЦА и выбрать характеристики таргетинга.
Однако, в результате вы будете давать
объявления, которые не приводят к
продажам, и тратить огромные деньги на
оплату услуг социальной сети.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Несмотря на
большие усилия, которые прилагает
</FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3><SPAN LANG="en-US">Instagram</SPAN></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>
(Фейсбук) для определения критериев
таргетинга, стандартные варианты не
позволяют точно адресовать объявление
заинтересованным лицам. Например,
сужение по интересам в поисковых запросах
позволяет ограничить аудиторию 25 млн.
человек. Такая аудитория&nbsp;слишком
огромна&nbsp;для мелкого рекламодателя
и не приведёт к росту продаж.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Кроме того,
возможны ошибки тартегинга из-за указания
в качестве критериев нишевых секторов
и отраслей, которые не пользуются
популярностью или не слишком конкретны.
Также&nbsp;ориентирование на интересы
пользователя, которые устарели. Например,
если человек уже не занимается ремонтом
машин, ему будут неинтересны объявления,
связанные с продажей запчастей.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Решение этой
проблемы — тартегирование объявлений,
начиная с базового уровня потребностей
без ориентации на случайные интересы.
Такой подход более сложен, однако
впоследствии он не только даст рост
конверсии объявлений, но и позволит
уменьшить стоимость их показа за счёт
увеличения баллов релевантности.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Итак, </FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3><B>существует
