<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>Способ
4:&nbsp;Привлечение новой аудитории с
тестированием эффективности разных
способов рекламы</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<IMG SRC="converted_file_decbe518_html_925bb522.jpg" NAME="Рисунок 1" ALIGN=BOTTOM WIDTH=300 HEIGHT=142 BORDER=0></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>После создания
правильных критериев отбора ЦА с помощью
инструментов Фейсбук, можно проверять
эффективность запущенной рекламной
компании и выбирать наиболее перспективные
направления.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Не стоит
увлекаться стандартными способами
проверки эффективности A / B, так как они
не всегда работают. Чтобы собрать нужную
статистику для такого анализа, необходимо
сначала достичь конверсии в 1000 и более
человек.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Более
эффективен тест A / B / N, с помощью которого
можно проверить различные варианты
рекламы и страниц. В A / B можно изменить
только одну из переменных объявления,
а A / B / N является более общим и позволяет
тестировать все коммерческие предложения
на разных страницах.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Например,
хостинг-сервис Kinsta позволяет проводить
тесты, размещая рекламные объявления
с разными предложениями. В них отражается
конкретная выгода, которую дает
реализуемый продукт, а переадресация
производится на посадочную страницу,
которая обыгрывает нишевый интерес.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Сами объявления
размещаются в нескольких вариантах на
разных целевых страницах и с разными
фильтрами ЦА. Это&nbsp;позволяет точно
отследить эффект от каждого варианта.
Кроме того, есть возможность собрать
статистику о времени нахождения на
странице, показатель отказов, возвраты
и т. п.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>Заключение</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Ни одна другая
рекламная площадка не позволяет настолько
