целевой аудитории на основе нишевых
интересов</B></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>.
Рассмотрим каждый их них поподробнее.</FONT></FONT></P>
<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>Способ
1:&nbsp;Определение связей с внешними
