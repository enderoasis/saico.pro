<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Социальная
сеть Quora является бездонным источником
необходимых данных, которые могут
использоваться для таргетинга по
интересам в </FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3><SPAN LANG="en-US">Instagram</SPAN></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>
(Фейсбук). Фактически, все запросы в
