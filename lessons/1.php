 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>Хэштег - это
 персонаж, который используется для
 группировки информации в социальных
 сетях и блогах. </FONT></FONT>
 </P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>Возможности
 хэштега:</FONT></FONT></P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>- излагает
 основной смысл текста, отражает основное
 значение,</FONT></FONT></P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>- собирать
 информацию по темам,</FONT></FONT></P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>- мгновенно
 находит интересную тему.</FONT></FONT></P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>Хэштег
 помечается «#», и когда он находится
 перед одним или несколькими словами
 (когда между словами нет пробела), он
 превращается в ссылку. Проходя по этой
 ссылке, вы собираете всю информацию на
 эту тему. </FONT></FONT>
 </P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>Как правильно
 написать хэштег?</FONT></FONT></P>
 <P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
 <FONT FACE="Times New Roman, serif"><FONT SIZE=3>Хэштег также
 может быть написан на кириллице или
 латинице. Слово или фраза должны быть
 написаны сразу после символа # без
 пробелов. А между хэштегами должен быть
 пробел. Если вы пишете хэштег, используя
  несколько слов, то тогда пробел не
 ставите (пишется слитно). Вы комбинируете
 слова и добавляете нижнее подчеркивание
 между ними. Например: </FONT></FONT><FONT COLOR="#ff0000"><FONT FACE="Times New Roman, serif"><FONT SIZE=3>#SMMмарафон
 #Меняюсьс</FONT></FONT></FONT><FONT COLOR="#ff0000"><FONT FACE="Times New Roman, serif"><FONT SIZE=3><SPAN LANG="en-US">saicoeducation</SPAN></FONT></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>.
 В социальной сети Instagram можно писать до
 30 хэштегов. Лучше всего писать не более
 5-7 хэштегов. Это поможет вам более точно
 определить и найти информацию.</FONT></FONT></P>
