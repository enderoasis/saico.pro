<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Эту социальную
сеть часто используют как источник
распространения маркетинговой информации
о конкретном бренде. Однако её можно
применять и для поиска ответов, которые
потом пригодятся, чтобы заинтересовать
потенциального клиента и превратить
