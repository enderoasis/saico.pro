<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>1. Закрытие
аккаунтов Инстаграм от посторонних</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<IMG SRC="7__html_3cf68b5e.jpg" NAME="Рисунок 4" ALIGN=BOTTOM WIDTH=165 HEIGHT=165 BORDER=0></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Владелец
аккаунта в Инстаграм может запретить
доступ к контенту, к примеру,
незарегистрированным пользователям
или пользователям, которые не являются
его подписчиками. В результате, человеку,
чтобы ознакомиться с ассортиментом
ваших товаров, необходимо регистрироваться
в соц. сети или подписываться на аккаунт
(плюс еще ждать подтверждения на подписку
с вашей стороны). А это дополнительные
действия и сложности, которые просто
несовместимы с эффективным продвижением.
Чем больше препятствий вы будете
создавать на пути потенциального
клиента, тем меньше шансов, что он станет
клиентом реальным. Скорее всего, человек
просто подпишется на уже открытый
аккаунт вашего конкурента.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Это базовая
ошибка, но, к сожалению, время от времени,
мы все еще наталкиваемся на такие вот
закрытые аккаунты брендов. Если закрытые
аккаунты для личных профилей — это
нормально, то для компаний это, как
минимум, странно.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3><I>Фишка для
самых любопытных.</I></FONT></FONT><FONT FACE="Times New Roman, serif"><FONT SIZE=3>
Если вам очень интересно посмотреть,
что скрывается за скрытым аккаунтом в
Инстаграм, то вы можете погуглить всякие
программки типа «private profile viewer on Instagram»
и Гугл вам выдаст массу программ, которые
помогут открыть для просмотра нужные
профили.</FONT></FONT></P>
<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>2. Минимальное
количество контактных данных или их
полное отсутствие</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Потенциальные
клиенты должны знать, каким именно
способом с вами можно связаться, чтобы
задать вопрос или купить ваш продукт
или услугу. Поэтому, в идеале, следует
указывать несколько способов связи, к
примеру, vk, email, телефон, адрес. Некоторые
владельцы аккаунтов в Инстаграм указывают
только один способ связи, к примеру,
Skype или WhatsApp, не думая о том, что далеко
не все клиенты пользуются данными
программами.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Вот один
реальный пример того, что делать
нежелательно. Кстати, лайки здесь
накрученные, так как аккаунт совсем
дохлый. Не обижайтесь на меня админы
аккаунта за эти слова, но на данный
момент он просто неэффективен. В описании
предлагается связь с компанией через
WhatsApp или Viber. У меня, например, нет ни
того, ни другого. Что мне делать? Звонить
я не буду. Эл. почта или САЙТ! не указаны.
Честно говоря, у меня, как потенциального
клиента, этот аккаунт не вызывает доверия
и я его просто закрою и пойду искать
ободки в другом месте.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
