<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>После
регистрации и входа в аккаунт, необходимо
найти соответствующую тему в ветке
обсуждений, либо решение конкретного
вопроса, и проанализировать записи в
ней. Это позволит выявить наиболее
популярные ключевые слова, а также
выделить «минус слова», которые помогут
точнее таргетировать объявление. Таким
образом, можно показывать объявления
только заинтересованной аудитории и
исключить из ЦА людей, которым не
интересен продукт или услуга.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=3>Кроме того,
Quora позволяет выявить заинтересованность
людей, связанных с этой темой, в конкретных
продуктах и решениях. А также определить
«боли», преодоление которых также
помогает повысить конверсию объявлений.</FONT></FONT></P>
<P STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
<FONT FACE="Times New Roman, serif"><FONT SIZE=5><B>Способ
3:&nbsp;Поиск связанных интересов целевой
аудитории в Фейсбук</B></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-top: 0.19in; margin-bottom: 0.19in; line-height: 100%">
